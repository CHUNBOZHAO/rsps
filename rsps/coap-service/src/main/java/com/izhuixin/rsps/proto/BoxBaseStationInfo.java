package com.izhuixin.rsps.proto;

import java.util.ArrayList;
import java.util.List;

public class BoxBaseStationInfo {

    String boxid;
    short mcc;
    byte mnc;
    byte type;
    List<Cell> cells = new ArrayList<>();
    
    public String getBoxid() {
        return boxid;
    }

    public void setBoxid(String boxid) {
        this.boxid = boxid;
    }

    public short getMcc() {
        return mcc;
    }

    public void setMcc(short mcc) {
        this.mcc = mcc;
    }

    public byte getMnc() {
        return mnc;
    }

    public void setMnc(byte mnc) {
        this.mnc = mnc;
    }

    public byte getType() {
        return type;
    }

    public void setType(byte type) {
        this.type = type;
    }

    public List<Cell> getCells() {
        return cells;
    }

    public void setCells(List<Cell> cells) {
        this.cells = cells;
    }

    @Override
    public String toString() {
        return "CellsInfo{" +
                "mcc=" + mcc +
                ", mnc=" + mnc +
                ", type=" + type +
                ", cells=" + cells +
                '}';
    }
}
