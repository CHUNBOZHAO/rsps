package com.izhuixin.rsps.common;

/***
 * 地址簿
 */
public class AddressBookVO {

    /** id */
    private Long id;

    /** 名称 */
    private String name;

    /** 联系电话 */
    private String tel;

    /** 省 */
    private String areaProvince;

    /** 市 */
    private String areaCity;

    /** 区/县 */
    private String areaCounty;

    /** 详细地址 */
    private String detailAddress;

    /** 区域代码 */
    private String areaCode;

    /** 地址 */
    private String wholeAddress;

    /** 默认地址 */
    private Boolean defaultSelect;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getTel() {
        return tel;
    }

    public void setTel(String tel) {
        this.tel = tel;
    }

    public String getAreaProvince() {
        return areaProvince;
    }

    public void setAreaProvince(String areaProvince) {
        this.areaProvince = areaProvince;
    }

    public String getAreaCity() {
        return areaCity;
    }

    public void setAreaCity(String areaCity) {
        this.areaCity = areaCity;
    }

    public String getAreaCounty() {
        return areaCounty;
    }

    public void setAreaCounty(String areaCounty) {
        this.areaCounty = areaCounty;
    }

    public String getDetailAddress() {
        return detailAddress;
    }

    public void setDetailAddress(String detailAddress) {
        this.detailAddress = detailAddress;
    }

    public String getAreaCode() {
        return areaCode;
    }

    public void setAreaCode(String areaCode) {
        this.areaCode = areaCode;
    }

    public String getWholeAddress() {
        return wholeAddress;
    }

    public void setWholeAddress(String wholeAddress) {
        this.wholeAddress = wholeAddress;
    }

    public Boolean getDefaultSelect() {
        return defaultSelect;
    }

    public void setDefaultSelect(Boolean defaultSelect) {
        this.defaultSelect = defaultSelect;
    }
}
