package com.izhuixin.rsps.common.dao;

/**
 * 线路关联中转用户信息
 */
public class RelatedSysUserInfo {

    /** 自增ID */
    private Long id;

    /** 用户ID */
    private String userId;

    /** 用户名 */
    private String userName;

    /** 真实姓名 */
    private String realName;

    /** 昵称 */
    private String nickname;

    /** 用户等级 */
    private Byte level;

    /** 父节点名称 */
    private String pNickname;

    /** 线路ID  */
    private String lineId;

    /** 是否关联 */
    private boolean lineRelated;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getUserId() {
        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public String getRealName() {
        return realName;
    }

    public void setRealName(String realName) {
        this.realName = realName;
    }

    public String getNickname() {
        return nickname;
    }

    public void setNickname(String nickname) {
        this.nickname = nickname;
    }

    public Byte getLevel() {
        return level;
    }

    public void setLevel(Byte level) {
        this.level = level;
    }

    public String getpNickname() {
        return pNickname;
    }

    public void setpNickname(String pNickname) {
        this.pNickname = pNickname;
    }

    public String getLineId() {
        return lineId;
    }

    public void setLineId(String lineId) {
        this.lineId = lineId;
    }

    public boolean isLineRelated() {
        return lineRelated;
    }

    public void setLineRelated(boolean lineRelated) {
        this.lineRelated = lineRelated;
    }
}
