package com.izhuixin.rsps.common.constant;

public enum BoxParamType {
    STOP(0,"中断"),START(1,"使能");

    BoxParamType(Integer index,String desc){
        this.desc = desc;
        this.index = index;
    }
    private Integer index;
    private String desc;

    public Integer getIndex() {
        return index;
    }

    public void setIndex(Integer index) {
        this.index = index;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }
}
