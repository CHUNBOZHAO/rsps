package com.izhuixin.rsps.common.dto;

import com.google.common.collect.Lists;

import java.util.List;

/**
 * 出货信息
 */
public class ShipmentInfoDTO {

    private int totalCount;

    private int fetchCount;

    private int signCount;

    private int recycleCount;

    private int gohomeCount;

    private List<String[]> datas = Lists.newArrayList();

    public int getTotalCount() {
        return totalCount;
    }

    public void setTotalCount(int totalCount) {
        this.totalCount = totalCount;
    }

    public int getFetchCount() {
        return fetchCount;
    }

    public void setFetchCount(int fetchCount) {
        this.fetchCount = fetchCount;
    }

    public int getSignCount() {
        return signCount;
    }

    public void setSignCount(int signCount) {
        this.signCount = signCount;
    }

    public int getRecycleCount() {
        return recycleCount;
    }

    public void setRecycleCount(int recycleCount) {
        this.recycleCount = recycleCount;
    }

    public int getGohomeCount() {
        return gohomeCount;
    }

    public void setGohomeCount(int gohomeCount) {
        this.gohomeCount = gohomeCount;
    }

    public List<String[]> getDatas() {
        return datas;
    }

    public void setDatas(List<String[]> datas) {
        this.datas = datas;
    }
}
