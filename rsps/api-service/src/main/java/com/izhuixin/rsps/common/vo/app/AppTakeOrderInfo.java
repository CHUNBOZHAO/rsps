package com.izhuixin.rsps.common.vo.app;

import java.io.Serializable;
import java.util.List;

/**
 * 揽货订单基本信息
 */
public class AppTakeOrderInfo implements Serializable {

    /** 订单ID */
    private String orderId;

    /** 客户ID（寄件人ID） */
    private String customId;

    /** 寄件人姓名 */
    private String sender;

    /** 寄件人电话 */
    private String senderTel;

    /** 寄件人地址 */
    private String senderAddress;

    /** 收件人姓名 */
    private String receiver;

    /** 收件人电话 */
    private String receiverTel;

    /** 收件人地址 */
    private String receiverAddress;

    /** 备注 */
    private String remark;

    /** 所需包装箱 */
    private List<AppBoxTypeInfo> boxes;

    public String getOrderId() {
        return orderId;
    }

    public void setOrderId(String orderId) {
        this.orderId = orderId;
    }

    public String getCustomId() {
        return customId;
    }

    public void setCustomId(String customId) {
        this.customId = customId;
    }

    public String getSender() {
        return sender;
    }

    public void setSender(String sender) {
        this.sender = sender;
    }

    public String getSenderTel() {
        return senderTel;
    }

    public void setSenderTel(String senderTel) {
        this.senderTel = senderTel;
    }

    public String getSenderAddress() {
        return senderAddress;
    }

    public void setSenderAddress(String senderAddress) {
        this.senderAddress = senderAddress;
    }

    public String getReceiver() {
        return receiver;
    }

    public void setReceiver(String receiver) {
        this.receiver = receiver;
    }

    public String getReceiverTel() {
        return receiverTel;
    }

    public void setReceiverTel(String receiverTel) {
        this.receiverTel = receiverTel;
    }

    public String getReceiverAddress() {
        return receiverAddress;
    }

    public void setReceiverAddress(String receiverAddress) {
        this.receiverAddress = receiverAddress;
    }

    public String getRemark() {
        return remark;
    }

    public void setRemark(String remark) {
        this.remark = remark;
    }

    public List<AppBoxTypeInfo> getBoxes() {
        return boxes;
    }

    public void setBoxes(List<AppBoxTypeInfo> boxes) {
        this.boxes = boxes;
    }
}
