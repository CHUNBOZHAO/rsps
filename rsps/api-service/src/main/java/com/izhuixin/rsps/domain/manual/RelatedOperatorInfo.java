package com.izhuixin.rsps.domain.manual;

/***
 * 线路关联
 */
public class RelatedOperatorInfo {

    /** 自增ID */
    private Long id;

    /** 用户ID */
    private String operatorId;

    /** 账号 */
    private String userName;

    /** 真实姓名 */
    private String realName;

    /** 昵称 */
    private String tel;

    /** 所属节点 */
    private Byte userId;

    /** 父节点名称 */
    private String parentNickName;

    /** 线路ID  */
    private String lineId;

    /** 线路关联 */
    private boolean lineRelated;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getOperatorId() {
        return operatorId;
    }

    public void setOperatorId(String operatorId) {
        this.operatorId = operatorId;
    }

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public String getRealName() {
        return realName;
    }

    public void setRealName(String realName) {
        this.realName = realName;
    }

    public String getTel() {
        return tel;
    }

    public void setTel(String tel) {
        this.tel = tel;
    }

    public Byte getUserId() {
        return userId;
    }

    public void setUserId(Byte userId) {
        this.userId = userId;
    }

    public String getParentNickName() {
        return parentNickName;
    }

    public void setParentNickName(String parentNickName) {
        this.parentNickName = parentNickName;
    }

    public boolean isLineRelated() {
        return lineRelated;
    }

    public void setLineRelated(boolean lineRelated) {
        this.lineRelated = lineRelated;
    }

    public String getLineId() {
        return lineId;
    }

    public void setLineId(String lineId) {
        this.lineId = lineId;
    }
}
