package com.izhuixin.rsps.common.vo.web;

import java.util.List;

/**
 * 客户ID
 */
public class OrderInfo {

    /** 订单ID */
    private Integer id;

    /** WMS的订单ID */
    private String orderId;

    /** 订单状态 配货、运输中、已签收、拒签  */
    private Byte stauts;

    /** 配送员信息 */
    private List<DriverInfo> drivers;

    /** 客户基本信息 */
    private CustomerInfoVO customerInfo;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getOrderId() {
        return orderId;
    }

    public void setOrderId(String orderId) {
        this.orderId = orderId;
    }

    public Byte getStauts() {
        return stauts;
    }

    public void setStauts(Byte stauts) {
        this.stauts = stauts;
    }

    public List<DriverInfo> getDriverInfos() {
        return drivers;
    }

    public void setDriverInfos(List<DriverInfo> driverInfos) {
        this.drivers = driverInfos;
    }

    public CustomerInfoVO getCustomerInfo() {
        return customerInfo;
    }

    public void setCustomerInfo(CustomerInfoVO customerInfo) {
        this.customerInfo = customerInfo;
    }
}
