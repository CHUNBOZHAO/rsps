package com.izhuixin.rsps.common.vo.app;

import java.util.List;

/**
 * 包装箱位置请求消息体
 */
public class AppReqQueryBoxLocation extends AppReqBase {

    /** 经度 */
    private Double longitude;

    /** 纬度 */
    private Double latitude;

    /** 坐标类型 */
    private Byte coordinateType;

    /** 半径(米) */
    private Double radius;

    /** 包装箱状态 */
    private List<Byte> status;

    /** 当前页 */
    private Integer pageIndex;

    /** 每页数量 */
    private Integer pageSize;

    public Double getLongitude() {
        return longitude;
    }

    public void setLongitude(Double longitude) {
        this.longitude = longitude;
    }

    public Double getLatitude() {
        return latitude;
    }

    public void setLatitude(Double latitude) {
        this.latitude = latitude;
    }

    public Byte getCoordinateType() {
        return coordinateType;
    }

    public void setCoordinateType(Byte coordinateType) {
        this.coordinateType = coordinateType;
    }

    public Integer getPageIndex() {
        return pageIndex;
    }

    public void setPageIndex(Integer pageIndex) {
        this.pageIndex = pageIndex;
    }

    public Integer getPageSize() {
        return pageSize;
    }

    public void setPageSize(Integer pageSize) {
        this.pageSize = pageSize;
    }

    public Double getRadius() {
        return radius;
    }

    public void setRadius(Double radius) {
        this.radius = radius;
    }

    public List<Byte> getStatus() {
        return status;
    }

    public void setStatus(List<Byte> status) {
        this.status = status;
    }
}
