package com.izhuixin.rsps.common.constant;

/**
 * 用户类型
 */
public enum ManagerUserPermission {

    ROLE_MAIN_SHOW("ROLE_MAIN_SHOW", "入口"),
    ROLE_HOME_SHOW("ROLE_HOME_SHOW", "总览"),
    ROLE_USER_SHOW("ROLE_USER_SHOW", "配送员管理"),
    ROLE_LINE_SHOW("ROLE_LINE_SHOW", "线路管理"),
    ROLE_SYS_USER_SHOW("ROLE_SYS_USER_SHOW", "管理用户管理"),
    ;

    private String name;

    private String descr;

    ManagerUserPermission(String name, String descr) {
        this.name = name;
        this.descr = descr;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDescr() {
        return descr;
    }

    public void setDescr(String descr) {
        this.descr = descr;
    }
}
