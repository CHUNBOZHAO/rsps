package com.izhuixin.rsps.constants;

public enum CommonSendAckStatus {

    SUCCESS(1,"成功"), FAILURE(2,"失败");

    CommonSendAckStatus(Integer index, String desc) {
        this.index = index;
        this.desc = desc;
    }

    private Integer index;
    private String desc;

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }

    public Integer getIndex() {
        return index;
    }

    public void setIndex(Integer index) {
        this.index = index;
    }
}
