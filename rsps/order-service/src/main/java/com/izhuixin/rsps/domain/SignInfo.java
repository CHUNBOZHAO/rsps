package com.izhuixin.rsps.domain;

import java.io.Serializable;
import java.util.List;

public class SignInfo implements Serializable {
    private String Jobno;
    private String CustJobno;
    private String SignDateTime;
    private String SignDriverNO;
    //拒收原因和拒收数量
    private String EXTCOL1;
    private String EXTCOL2;
    //图片路径
    private List<String> EXTCOL3;

    //签收人
    private String SignDriverName;

    //配载单号
    private String eirNo;

    public String getJobno() {
        return Jobno;
    }

    public void setJobno(String jobno) {
        Jobno = jobno;
    }

    public String getCustJobno() {
        return CustJobno;
    }

    public void setCustJobno(String custJobno) {
        CustJobno = custJobno;
    }

    public String getSignDriverNO() {
        return SignDriverNO;
    }

    public void setSignDriverNO(String signDriverNO) {
        SignDriverNO = signDriverNO;
    }

    public String getSignDateTime() {
        return SignDateTime;
    }

    public void setSignDateTime(String signDateTime) {
        SignDateTime = signDateTime;
    }

    public String getEXTCOL1() {
        return EXTCOL1;
    }

    public void setEXTCOL1(String EXTCOL1) {
        this.EXTCOL1 = EXTCOL1;
    }

    public String getEXTCOL2() {
        return EXTCOL2;
    }

    public void setEXTCOL2(String EXTCOL2) {
        this.EXTCOL2 = EXTCOL2;
    }

    public List<String> getEXTCOL3() {
        return EXTCOL3;
    }

    public void setEXTCOL3(List<String> EXTCOL3) {
        this.EXTCOL3 = EXTCOL3;
    }

    public String getSignDriverName() {
        return SignDriverName;
    }

    public void setSignDriverName(String signDriverName) {
        SignDriverName = signDriverName;
    }

    public String getEirNo() {
        return eirNo;
    }

    public void setEirNo(String eirNo) {
        this.eirNo = eirNo;
    }
}
